import numpy as np

# Convert a list to an array
my_list = [1, 2, 3, 4, 5]
array_from_list = np.array(my_list)
print("Array from list:", array_from_list)

# Convert a tuple to an array
my_tuple = (6, 7, 8, 9, 10)
array_from_tuple = np.array(my_tuple)
print("Array from tuple:", array_from_tuple)
